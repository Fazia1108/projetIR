package com.example.fanfarehub.model;

public class StatutParticipation {
    private int idStatut;
    private String libelleStatut;
    private String couleur;

    public int getIdStatut() { return idStatut; }
    public void setIdStatut(int idStatut) { this.idStatut = idStatut; }
    public String getLibelleStatut() { return libelleStatut; }
    public void setLibelleStatut(String libelleStatut) { this.libelleStatut = libelleStatut;}
    public String getCouleur() { return couleur; }
    public void setCouleur(String couleur) { this.couleur = couleur; }

}
